load("shiny-webtool/data/isp_generation_output.rda")

# ----- Historical leg: FY2010–FY2025, coerced to ISP 2026 canonical names ---
#
# add_coerced_tech_cat(col = "fueltech") maps OpenNEM codes → ISP 2026 names.
# Storage-load fueltechs (battery_charging, pumps) map to "utility-scale storage
# load" which we then exclude — keeping only generation, not consumption.

hist_leg <- energy_data |>
  filter(financial_year >= 2010, financial_year <= 2025) |>
  add_coerced_tech_cat(col = "fueltech") |>
  filter(!str_detect(coerced_tech_cat, "load")) |>
  group_by(financial_year, coerced_tech_cat) |>
  summarise(value_gwh = sum(value_gwh), .groups = "drop") |>
  mutate(data_source = "historical")

# ----- ISP leg: FY2027–FY2050, step change ODP (cdp4), NEM-wide sum ---------
#
# add_coerced_tech_cat(col = "technology") maps ISP 2026 names to themselves
# (self-mapping), so coerced_tech_cat == technology for 2026 draft data.
# Older ISP version names would also be correctly coerced if included.

isp_leg <- isp_generation_output |>
  filter(
    source   == "2026_draft",
    scenario == "step change",
    cdp      == "cdp4"
  ) |>
  add_coerced_tech_cat(col = "technology") |>
  filter(!str_detect(coerced_tech_cat, "load")) |>
  group_by(financial_year = year, coerced_tech_cat) |>
  summarise(value_gwh = sum(value), .groups = "drop") |>
  mutate(data_source = "projected")

# ----- Combine and join colours from util_table ------------------------------

combined_energy <- bind_rows(hist_leg, isp_leg) |>
  mutate(
    coerced_tech_cat = factor(coerced_tech_cat, levels = util_table$technology),
    data_source      = factor(data_source, levels = c("historical", "projected"))
  )

