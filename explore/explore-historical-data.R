# ===== Data structure notes for Shiny integration =============================
#
# Key observations:
#
# 1. TEMPORAL ALIGNMENT
#    - Historical data: FY2009–FY2025 (monthly, aggregated to annual for charts)
#    - ISP projections: FY2025–FY2050 (5-year steps)
#    - Overlap year: FY2025 – use historical as "actuals", ISP as "projection start"
#
# 2. TECHNOLOGY MAPPING (OpenNEM → ISP)
#    The fueltech_lookup above maps OpenNEM codes to display labels.
#    For Shiny overlay, a crosswalk to ISP technology names is needed:
#
#    OpenNEM fueltech     → ISP technology (util_table)
#    ──────────────────────────────────────────────────
#    coal_black           → black coal
#    coal_brown           → brown coal
#    gas_ccgt             → mid-merit gas
#    gas_steam            → mid-merit gas (or peaking gas+liquids)
#    gas_ocgt             → flexible gas (or peaking gas+liquids)
#    gas_recip            → flexible gas
#    gas_wcmg             → flexible gas
#    distillate           → peaking gas+liquids
#    bioenergy_biomass    → biomass
#    hydro                → hydro
#    wind                 → wind
#    solar_utility        → utility-scale solar
#    solar_rooftop        → distributed pv
#    battery_discharging  → utility-scale storage
#    battery_charging     → utility storage load (negative)
#    pumps                → (no direct ISP equivalent – pumped hydro load)
#
# 3. UNIT CONVERSION
#    - OpenNEM energy: MWh → divide by 1,000 for GWh (matches ISP generation_output)
#    - OpenNEM power (metric="power"): sum of 5-min MW readings, NOT average MW.
#      To get avg MW: value / (days_in_month * 24 * 12)
#    - OpenNEM demand_energy: appears to be in GWh despite "MWh" unit label.
#    - demand_energy network_region parses as e.g. "energy_NSW1" – needs cleanup.
#
# 4. SHINY INTEGRATION OPTIONS
#    Option A: "Historical context" tab
#      - Standalone tab with historical-only charts (energy, emissions, price)
#      - Separate from ISP projections tabs
#      - Simplest to implement
#
#    Option B: Overlay on existing tabs
#      - Add historical actuals as shaded "actual" region on ISP projection charts
#      - Requires technology crosswalk and unit alignment
#      - More informative but more complex
#
#    Option C: Combined timeline chart
#      - x-axis spans FY2009 to FY2050
#      - Left portion = historical actuals, right = ISP projections
#      - Vertical dashed line at current year / ISP start year
#      - Most powerful for communicating trajectory
#
# 5. RECOMMENDED NEXT STEP
#    Create fueltech_to_isp_map lookup table (crosswalk) and test Option C
#    using a single region/scenario combination as a proof-of-concept.
#
