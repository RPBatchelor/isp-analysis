# ===== Helper: period and financial year columns ==============================
#
# period_start is POSIXct in AEST (Etc/GMT-10), marking the first moment of
# the monthly period (e.g. 2009-07-01 = July 2009).
#
# Adds:
#   period_end     – last second of the same calendar month (AEST)
#   month          – calendar month integer (1–12)
#   year           – calendar year integer
#   year_month     – "YYYY-MM" label for the period
#   financial_year – Australian FY ending June; July–Dec map to FY = year + 1,
#                    Jan–June map to FY = year  (e.g. Jul 2009 → FY2010)

add_period_columns <- function(df) {
  df |>
    mutate(
      period_end     = ceiling_date(period_start + seconds(1), "month") - seconds(1),
      month          = month(period_start),
      year           = year(period_start),
      year_month     = format(period_start, "%Y-%m"),
      financial_year = if_else(month >= 7L, year + 1L, year)
    )
}


# ===== Prepare energy data ====================================================

energy_data <- nem_historical_network_data |>
  filter(metric == "energy") |>
  add_period_columns() |>
  left_join(fueltech_lookup, by = "fueltech") |>
  mutate(
    value_gwh = value / 1000,           # MWh → GWh
    region = str_remove(network_region, "1$")  # NSW1 → NSW
  )

# Annual NEM-wide energy by fueltech
energy_annual_nem <- energy_data |>
  filter(!is.na(label)) |>
  group_by(financial_year, fueltech, label, colour, tech_type, order) |>
  summarise(value_gwh = sum(value_gwh), .groups = "drop") |>
  mutate(
    label = factor(label, levels = fueltech_lookup$label),
    value_twh = value_gwh / 1000
  )

# Annual energy by fueltech and region
energy_annual_region <- energy_data |>
  filter(!is.na(label)) |>
  group_by(financial_year, region, fueltech, label, colour, tech_type, order) |>
  summarise(value_gwh = sum(value_gwh), .groups = "drop") |>
  mutate(label = factor(label, levels = fueltech_lookup$label))


